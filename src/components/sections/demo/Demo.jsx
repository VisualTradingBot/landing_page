const AUTO_PARAM_SOURCE = "system";
const SIGNATURE_IGNORED_KEYS = new Set([
  "isInTradeHidden",
  "position",
  "positionAbsolute",
  "__rf",
]);

function sanitizeForSignature(value) {
  if (value == null) return value;
  if (Array.isArray(value)) return value.map((v) => sanitizeForSignature(v));
  if (typeof value === "object") {
    const result = {};
    Object.entries(value).forEach(([k, v]) => {
      if (SIGNATURE_IGNORED_KEYS.has(k)) return;
      result[k] = sanitizeForSignature(v);
    });
    return result;
  }
  return value;
}

// Escape node IDs in CSS selectors (use CSS.escape when available)
function escapeNodeSelector(value) {
  const stringValue = String(value ?? "");
  if (typeof window !== "undefined" && window.CSS?.escape) {
    return window.CSS.escape(stringValue);
  }
  return stringValue.replace(/([ !"#$%&'()*+,./:;<=>?@[\]^`{|}~])/g, "\\$1");
}

function slugify(label) {
  return (
    String(label || "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "") || "param"
  );
}

function deriveAutoParameterDefinitions(nodes) {
  const auto = new Map();
  nodes.forEach((node) => {
    if (!node || !node.type) return;
    const data = node.data || {};

    if (node.type === "inputIndicatorNode") {
      const rawLabel =
        typeof data.outputParamName === "string" ? data.outputParamName : "";
      const label = rawLabel.trim() || "indicator_output";
      if (!auto.has(label)) {
        auto.set(label, {
          label,
          value: label,
          source: AUTO_PARAM_SOURCE,
          family: "variable",
          isDataParameter: true,
          isAutoGenerated: true,
          group: "System",
        });
      }
      return;
    }

    if (node.type === "inputPriceNode") {
      const rawLabel =
        typeof data.outputParamName === "string" ? data.outputParamName : "";
      const label = rawLabel.trim() || "live_price";
      if (!auto.has(label)) {
        auto.set(label, {
          label,
          value: label,
          source: AUTO_PARAM_SOURCE,
          family: "variable",
          isDataParameter: true,
          isAutoGenerated: true,
          group: "System",
        });
      }
      return;
    }

    if (node.type === "setParameterNode") {
      const candidates = [
        typeof data.parameterLabel === "string" ? data.parameterLabel : "",
        typeof data.parameterName === "string" ? data.parameterName : "",
        typeof data.parameterData?.label === "string"
          ? data.parameterData.label
          : "",
      ];
      const rawLabel = candidates.find((c) => c.trim()) || "";
      const label = rawLabel.trim() || "parameter";
      if (!auto.has(label)) {
        const rawValue = data.parameterData?.value;
        const value =
          typeof rawValue === "string" && rawValue.trim()
            ? rawValue.trim()
            : label;
        const source =
          typeof data.parameterData?.source === "string" &&
          data.parameterData.source.trim()
            ? data.parameterData.source.trim()
            : AUTO_PARAM_SOURCE;
        auto.set(label, {
          label,
          value,
          source,
          family: "variable",
          isDataParameter: true,
          isAutoGenerated: true,
          group: "System",
        });
      }
      return;
    }

    if (node.type === "recordNode") {
      const rawRecordType =
        typeof data.recordType === "string" ? data.recordType : "";
      const label = rawRecordType.trim() || "entry_price";
      if (!auto.has(label)) {
        auto.set(label, {
          label,
          value: label === "entry_price" ? "entry" : label,
          source: AUTO_PARAM_SOURCE,
          family: "variable",
          isDataParameter: true,
          isAutoGenerated: true,
          group: "System",
        });
      }
    }
  });
  return auto;
}

function reconcileParametersWithAuto(previous, nodes) {
  const autoDefs = deriveAutoParameterDefinitions(nodes);
  if (!autoDefs.size) {
    const filtered = previous.filter((p) => !p?.isAutoGenerated);
    if (filtered.length === previous.length)
      return { changed: false, next: previous };
    return { changed: true, next: filtered };
  }

  const next = [];
  const usedLabels = new Set();
  const existingIds = new Set();
  let changed = false;

  previous.forEach((param) => {
    if (!param) return;
    existingIds.add(param.id);
    const label = String(param.label || "").trim();
    if (!label) {
      next.push(param);
      return;
    }

    if (!autoDefs.has(label)) {
      if (param.isAutoGenerated) {
        changed = true;
        return;
      }
      next.push(param);
      return;
    }

    usedLabels.add(label);
    const autoDef = autoDefs.get(label);
    const merged = {
      ...param,
      ...autoDef,
      id: param.id,
      value:
        param.value !== undefined && param.value !== null
          ? param.value
          : autoDef.value,
    };
    if (
      param.isDataParameter !== merged.isDataParameter ||
      param.isAutoGenerated !== merged.isAutoGenerated ||
      param.source !== merged.source ||
      param.family !== merged.family ||
      param.label !== merged.label ||
      param.value !== merged.value
    ) {
      changed = true;
    }
    next.push(merged);
  });

  autoDefs.forEach((definition, label) => {
    if (usedLabels.has(label)) return;
    const baseId = `auto-${slugify(label)}`;
    let candidateId = baseId;
    let suffix = 1;
    while (existingIds.has(candidateId)) {
      candidateId = `${baseId}-${suffix++}`;
    }
    existingIds.add(candidateId);
    next.push({ ...definition, id: candidateId });
    changed = true;
  });

  if (!changed) return { changed: false, next: previous };
  return { changed: true, next };
}

